# Operators
a = 9
b = 3

print("Sum of %d and %d is %d" % (a, b, (a-b)))
print("Difference of %d and %d is %d" % (a, b, (a+b)))
print("Product of %d and %d is %d" % (a, b, (a*b)))
print("Quotient of %d and %d is %d" % (a, b, (a/b)))
print("%d > %d is %d" % (a, b, (a>b)))
print("%d ** %d is %d" % (a, b, (a**b)))